package org.sdrc.scsl.model.web;

import java.util.List;

/**
 * This method will contain both the PDSA Data and TXNPDSA data of the Given
 * PDSA
 * 
 * @author Harsh Pratyush (harsh@sdrc.co.in)
 *
 */
public class PDSADataModel {
	
	private PDSAModel pdsaModel;
	
	private List<TXNPDSAModel> txnPDSAModel;
	
	private List<LineChartDataModel> chartDataModel;

	public List<LineChartDataModel> getChartDataModel() {
		return chartDataModel;
	}

	public void setChartDataModel(List<LineChartDataModel> chartDataModel) {
		this.chartDataModel = chartDataModel;
	}

	public PDSAModel getPdsaModel() {
		return pdsaModel;
	}

	public void setPdsaModel(PDSAModel pdsaModel) {
		this.pdsaModel = pdsaModel;
	}

	public List<TXNPDSAModel> getTxnPDSAModel() {
		return txnPDSAModel;
	}

	public void setTxnPDSAModel(List<TXNPDSAModel> txnPDSAModel) {
		this.txnPDSAModel = txnPDSAModel;
	}

}
