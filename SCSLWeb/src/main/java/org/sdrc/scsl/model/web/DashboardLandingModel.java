package org.sdrc.scsl.model.web;

import java.util.List;

/**
 * @author Sarita Panigrahi, created on: 27-Sep-2017
 */
public class DashboardLandingModel {

	private List<BoxChart> boxCharts;
	private List<List<DashboardChartModel>> chartModels;
	private List<String> trendChartSvgs;

	public List<String> getTrendChartSvgs() {
		return trendChartSvgs;
	}

	public void setTrendChartSvgs(List<String> trendChartSvgs) {
		this.trendChartSvgs = trendChartSvgs;
	}

	public List<BoxChart> getBoxCharts() {
		return boxCharts;
	}

	public void setBoxCharts(List<BoxChart> boxCharts) {
		this.boxCharts = boxCharts;
	}
	public List<List<DashboardChartModel>> getChartModels() {
		return chartModels;
	}

	public void setChartModels(List<List<DashboardChartModel>> chartModels) {
		this.chartModels = chartModels;
	}

}
