package org.sdrc.scsl.model.web;

import java.util.List;
/**
 * This model will contain the  List mstEngagementScore and Facility List 
 * Facility List will be acccording to the user logged in the application
 * @author Harsh Pratyush (harsh@sdrc.co.in)
 *
 */
public class MSTEngagementScoreAndFacilityCollectionModel {
	
	private List<MSTEngagementScoreModel> mstEngagementScoreModel;
	
	private List<FacilityModel> facilityModel;

	public List<MSTEngagementScoreModel> getMstEngagementScoreModel() {
		return mstEngagementScoreModel;
	}

	public void setMstEngagementScoreModel(
			List<MSTEngagementScoreModel> mstEngagementScoreModel) {
		this.mstEngagementScoreModel = mstEngagementScoreModel;
	}

	public List<FacilityModel> getFacilityModel() {
		return facilityModel;
	}

	public void setFacilityModel(List<FacilityModel> facilityModel) {
		this.facilityModel = facilityModel;
	}

}
