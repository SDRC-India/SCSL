package org.sdrc.scsl.model.web;

import java.util.List;

/**
 * This model will conatin the Facility Name its respective Id and the List of
 * available timpeperiod for Engagement Score
 * 
 * @author Harsh Pratyush (harsh@sdrc.co.in)
 *
 */
public class FacilityModel {

	// this will contain areaId of the area
	private int facailityId;

	// this will contain name of the area
	private String facilityName;

	// this will contain list of timeperiod and respective data of score he filled (if he filled then)
	private List<TimePeriodModel> timeperiods;
	
	// This will contain the id of timepriod latest engagementscoresubmission of given facility
	private int timePeriodId;

	public int getTimePeriodId() {
		return timePeriodId;
	}

	public void setTimePeriodId(int timePeriodId) {
		this.timePeriodId = timePeriodId;
	}

	public int getFacailityId() {
		return facailityId;
	}

	public void setFacailityId(int facailityId) {
		this.facailityId = facailityId;
	}

	public String getFacilityName() {
		return facilityName;
	}

	public void setFacilityName(String facilityName) {
		this.facilityName = facilityName;
	}

	public List<TimePeriodModel> getTimeperiods() {
		return timeperiods;
	}

	public void setTimeperiods(List<TimePeriodModel> timeperiods) {
		this.timeperiods = timeperiods;
	}

}
